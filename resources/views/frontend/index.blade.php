                        error: function error(data) {
                            let errors = data.responseJSON,
                                msg = '<ul style="list-style: none;text-align: left;">';
                            $.each(errors.errors, function (key, value) {
                                msg += '<li>' + value[0] + '</li>';
                            });
                            msg += '</ul>';
                            $('.error-confirm').html(msg).fadeIn();
                        },
                    });
                }
            });
